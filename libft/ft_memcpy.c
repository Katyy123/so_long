#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)src;
	s2 = (unsigned char *)dst;
	if (!dst && !src)
		return (0);
	while (n--)
	{
		s2[n] = s1[n];
	}
	return (dst);
}
